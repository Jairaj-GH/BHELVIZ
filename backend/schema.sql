-- BHELVIZ — Oracle 19c Schema (Production)
-- Dual-layer encryption: Oracle TDE (at-rest) + AES-256-GCM (app-level)
-- All sensitive VARCHAR2 columns store AES-256-GCM ciphertext.
-- Decryption is client-side only — server never holds plaintext.
