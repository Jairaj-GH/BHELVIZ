-- ── VIEW: EMPLOYEE ATTENDANCE (ciphertext — no decryption) ───────────────
CREATE OR REPLACE VIEW employee_attendance_v AS
SELECT
    a.attendance_id,
    e.employee_no_enc,           -- ciphertext
    e.full_name_enc,             -- ciphertext
    d.dept_code,                 -- clear enum
    d.dept_name_enc,             -- ciphertext
    r.role_code,                 -- clear enum
    r.role_name,
    s.shift_code,                -- clear enum
    s.shift_name,
    a.att_date,             -- clear timestamp (needed for range queries)
    a.status_code,
    ast.status_name,
    a.attendance_penalty
FROM attendance a
JOIN employee              e   ON e.employee_id            = a.employee_id
JOIN department            d   ON d.department_id          = e.department_id
JOIN role_lu               r   ON r.role_code              = e.current_role_code
JOIN shift                 s   ON s.shift_id               = a.shift_id
JOIN status_lu  ast ON ast.status_code = a.status_code;


-- ── VIEW: EMPLOYEE DEPT ROLE ──────────────────────────────────────────────
CREATE OR REPLACE VIEW employee_dept_role_v AS
SELECT
    e.employee_id,
    e.employee_no_enc,
    e.full_name_enc,
    e.current_role_code,
    r.role_name,
    d.dept_code,
    d.dept_name_enc,
    e.hired_at_enc,
    e.active_flag
FROM employee e
JOIN department d ON d.department_id  = e.department_id
JOIN role_lu    r ON r.role_code      = e.current_role_code;


