-- ── ATTENDANCE (partitioned by month) ────────────────────────────────────
CREATE TABLE attendance (
    attendance_id          NUMBER GENERATED BY DEFAULT AS IDENTITY,
    employee_id            NUMBER       NOT NULL,
    shift_id               NUMBER       NOT NULL,
    att_date          TIMESTAMP(6) NOT NULL,                        -- clear (range queries)
    status_code VARCHAR2(30) NOT NULL,
    attendance_penalty     NUMBER(10,0) NULL,
    source_channel         VARCHAR2(30) NULL,
    device_id              VARCHAR2(64) NULL,
    created_at             TIMESTAMP(6) DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT pk_attendance          PRIMARY KEY (attendance_id, att_date),
    CONSTRAINT fk_att_employee        FOREIGN KEY (employee_id)            REFERENCES employee(employee_id),
    CONSTRAINT fk_att_shift           FOREIGN KEY (shift_id)               REFERENCES shift(shift_id),
    CONSTRAINT fk_att_status          FOREIGN KEY (status_code) REFERENCES status_lu(status_code),
    CONSTRAINT uq_att_business        UNIQUE (employee_id, shift_id, att_date),
    CONSTRAINT ck_att_penalty         CHECK (attendance_penalty IS NULL OR attendance_penalty < 0)
)
PARTITION BY RANGE (att_date)
INTERVAL (NUMTOYMINTERVAL(1, 'MONTH'))
(
    PARTITION p_2026_01 VALUES LESS THAN (TIMESTAMP '2026-02-01 00:00:00'),
    PARTITION p_2026_02 VALUES LESS THAN (TIMESTAMP '2026-03-01 00:00:00'),
    PARTITION p_2026_03 VALUES LESS THAN (TIMESTAMP '2026-04-01 00:00:00'),
    PARTITION p_2026_04 VALUES LESS THAN (TIMESTAMP '2026-05-01 00:00:00'),
    PARTITION p_2026_05 VALUES LESS THAN (TIMESTAMP '2026-06-01 00:00:00')
) TABLESPACE bhelviz_data;


-- ── TRIGGER: FALSE ATTENDANCE PENALTY ────────────────────────────────────
CREATE OR REPLACE TRIGGER trg_attendance_penalty
BEFORE INSERT OR UPDATE ON attendance
FOR EACH ROW
BEGIN
    IF :NEW.status_code = 'FALSE_PRESENT' THEN
        IF :NEW.attendance_penalty IS NULL THEN
            :NEW.attendance_penalty := -1;
        END IF;
        IF :NEW.attendance_penalty >= 0 THEN
            RAISE_APPLICATION_ERROR(-20001, 'False attendance penalty must be negative.');
        END IF;
    ELSE
        :NEW.attendance_penalty := NULL;
    END IF;
END;
/


