-- ── TABLESPACE (TDE-encrypted) ────────────────────────────────────────────
-- Execute as DBA before schema creation:
--
-- ADMINISTER KEY MANAGEMENT SET KEYSTORE OPEN
--   IDENTIFIED BY EXTERNAL STORE;
--
-- CREATE TABLESPACE bhelviz_data
--   DATAFILE '/oradata/bhelviz/bhelviz_data01.dbf' SIZE 500M AUTOEXTEND ON
--   ENCRYPTION USING AES256
--   DEFAULT STORAGE (ENCRYPT);
--
-- All tables should be created in this tablespace.


-- ── DEPARTMENT ────────────────────────────────────────────────────────────
CREATE TABLE department (
    department_id         NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    dept_code             VARCHAR2(30)  NOT NULL,
    dept_name_enc         VARCHAR2(512) NOT NULL,  -- AES-256-GCM ciphertext
    parent_department_id  NUMBER        NULL,
    created_at            TIMESTAMP(6)  DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT uq_dept_code    UNIQUE (dept_code),
    CONSTRAINT fk_dept_parent  FOREIGN KEY (parent_department_id)
                                REFERENCES department(department_id)
) TABLESPACE bhelviz_data;


-- ── ROLE LOOKUP ───────────────────────────────────────────────────────────
CREATE TABLE role_lu (
    role_code  VARCHAR2(30) PRIMARY KEY,
    role_name  VARCHAR2(100) NOT NULL,
    CONSTRAINT uq_role_name UNIQUE (role_name),
    CONSTRAINT ck_role_code CHECK (role_code IN ('EXECUTIVE','SUPERVISOR','WORKMAN'))
);


-- ── EMPLOYEE ──────────────────────────────────────────────────────────────
CREATE TABLE employee (
    employee_id          NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    employee_no_enc      VARCHAR2(512)  NOT NULL,  -- AES-256-GCM ciphertext (deterministic for uniqueness)
    full_name_enc        VARCHAR2(1024) NOT NULL,  -- AES-256-GCM ciphertext
    department_id        NUMBER         NOT NULL,
    current_role_code    VARCHAR2(30)   NOT NULL,
    employment_status_enc VARCHAR2(512) NOT NULL,  -- AES-256-GCM ciphertext
    hired_at_enc         VARCHAR2(256)  NOT NULL,  -- AES-256-GCM ciphertext (date serialised to ISO string)
    active_flag          CHAR(1)        DEFAULT 'Y' NOT NULL,
    CONSTRAINT uq_employee_no  UNIQUE (employee_no_enc),  -- deterministic enc preserves uniqueness
    CONSTRAINT fk_emp_dept     FOREIGN KEY (department_id)   REFERENCES department(department_id),
    CONSTRAINT fk_emp_role     FOREIGN KEY (current_role_code) REFERENCES role_lu(role_code),
    CONSTRAINT ck_emp_active   CHECK (active_flag IN ('Y','N'))
) TABLESPACE bhelviz_data;


