-- ── EMPLOYEE RELATION ─────────────────────────────────────────────────────
CREATE TABLE employee_relation (
    relation_id          NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    subject_employee_id  NUMBER       NOT NULL,
    object_employee_id   NUMBER       NOT NULL,
    relation_type_code   VARCHAR2(30) NOT NULL,
    valid_from           DATE         NOT NULL,
    valid_to             DATE         NULL,
    is_primary           CHAR(1)      DEFAULT 'N' NOT NULL,
    CONSTRAINT fk_rel_subject  FOREIGN KEY (subject_employee_id) REFERENCES employee(employee_id),
    CONSTRAINT fk_rel_object   FOREIGN KEY (object_employee_id)  REFERENCES employee(employee_id),
    CONSTRAINT fk_rel_type     FOREIGN KEY (relation_type_code)  REFERENCES employee_relation_type_lu(relation_type_code),
    CONSTRAINT ck_rel_not_self CHECK (subject_employee_id <> object_employee_id),
    CONSTRAINT ck_rel_primary  CHECK (is_primary IN ('Y','N'))
) TABLESPACE bhelviz_data;


-- ── EMPLOYEE ROLE HISTORY ─────────────────────────────────────────────────
CREATE TABLE employee_role_history (
